// JS condition statements

/*
- if statement
if (expression) { .....  }
- if else statement
if (expression) { ..... } else { ..... }
- if else if statement
if (expression1) { ..... } else if (expression2) { ..... } else { ..... }
*/

var a = 20;

if (a == 10) {
    console.log("a is equal to 10");
} else if (a == 15) {
    console.log("a is equal to 15");
} else if (a == 20) {
    console.log("a is equal to 20");
} else {
    console.log("a is not equal to 10, 15 or 20");
}